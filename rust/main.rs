fn main() {
 // Integer data types
 let integer_number: i32 = 42;
 let unsigned_integer: u64 = 100;

 // Floating-point data types
 let float_number: f64 = 3.14;

 // Boolean data type
 let is_rust_cool: bool = true;

 // Character data type
 let letter: char = 'A';

 // String data type
 let message: &str = "Hello, Rust!";

 // Displaying values
 println!("Integer: {}", integer_number);
 println!("Unsigned Integer: {}", unsigned_integer);
 println!("Float: {}", float_number);
 println!("Boolean: {}", is_rust_cool);
 println!("Character: {}", letter);
 println!("String: {}", message);
}
